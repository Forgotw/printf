#include <libft.h>

int	ft_intlen(int x)
{
	int	i;

	i = 0;
	if (x == 0)
		return (1);
	if (x < 0)
	{
		x *= -1;
		i++;
	}
	while (x != 0 )
	{
		x /= 10;
		i++;
	}
	return (i);
}
